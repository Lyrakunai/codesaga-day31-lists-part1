# Day 31 - Lists

# Creating a list
fruits = ["apple", "banana", "mango"]


# Accessing items
bag = ["pen","pencil","bottle"]
print("First item:", bag[0])
print("third item:",bag[2])


# Negative indexing
marks = [45,80,63,90]
print("last item:",marks[-1])


# Updating item
numbers = [2,3,6,8]
numbers[1] = 4
print("Updated list:", numbers)


# Adding item using append()
fruits = ["apple","banana","mango"]
fruits.append("orange")
print("After append:", fruits)


# Total items using len()
fruits = ["apple","banana","mango"]
print("Total items:", len(fruits))